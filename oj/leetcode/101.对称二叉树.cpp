// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    bool isSymmetric(TreeNode* root) {
        return dfs(root, root);
    }
    
private:
    bool dfs(TreeNode* root, TreeNode* root1){
        if(root == nullptr && root1 == nullptr) return true;
        if(root == nullptr || root1 == nullptr) return false;
        if(root->val != root1->val) return false;
        if(!dfs(root->left, root1->right)) return false;
        if(!dfs(root->right, root1->left)) return false;
        return true;
    }
};
